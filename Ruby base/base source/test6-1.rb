def perimeter_of_square(side_length)
  side_length * 4
end

def area_of_square(side_length)
  side_length * side_length
end

def perimeter_of_triangle(side1,side2,side3)
  side1 + side2 + 3
end

def area_of_triangle(base_width,height)
  base_width * height / 2
end

